// Утилита выбора случайной реплики из категории с учётом фазы
export function pickKaneLine(category, subcategory, phase = 'hero') {
  const cat = KANE_DIALOGUES[category];
  if (!cat) return null;

  // Пробуем специализированный для фазы
  let lines = cat[phase];
  // Иначе — subcategory
  if (!lines && subcategory) lines = cat[subcategory];
  // Иначе — any/all
  if (!lines) lines = cat.all ?? cat.first;
  if (!lines) return null;

  return lines[Math.floor(Math.random() * lines.length)];
}
